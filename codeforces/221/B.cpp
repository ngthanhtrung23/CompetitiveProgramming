bool good(int a, int b) {
    set<int> ca; ca.clear();
    while (a) {
        ca.insert(a % 10);
        a /= 10;
    }
    while (b) {
        if (ca.find(b % 10) != ca.end()) return true;
        b /= 10;
    }
    return false;
}

