void solve() {
    int nSmall, nBig; cin >> nSmall >> nBig;
    // Build small tree = a line:
    // 1 - 2 - ... - nSmall
    for (int i = 1; i < nSmall; ++i) {
        cout << i << ' ' << i+1 << endl;
    }
    int diam = nSmall - 1;

    // Longest path going through u & v
    // = subtree(u, v) + subtree(v, u) - distance(u, v)
    for (int turn = 0; turn < 222; ++turn) {
        int u = get_rand(1, nBig-1);
        int v = get_rand(u+1, nBig);

        cout << "distance " << u << ' ' << v << endl;
        int distance; cin >> distance;

        cout << "subtree " << u << ' ' << v << endl;
        int subtree_uv; cin >> subtree_uv;

        cout << "subtree " << v << ' ' << u << endl;
        int subtree_vu; cin >> subtree_vu;

        diam = max(diam, subtree_uv + subtree_vu - distance);
    }

    cout << "! " << diam << endl;
}
